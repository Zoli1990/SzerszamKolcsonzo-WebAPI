/**
 * Település lekérése irányítószám alapján
 * @param {string} iranyitoszam - 4 számjegyű irányítószám
 * @returns {string|null} Település neve vagy null
 */
export function getTelepulesFromIranyitoszam(iranyitoszam) {
  if (!iranyitoszam || iranyitoszam.length !== 4) {
    return null
  }
  return iranyitoszamMapping[iranyitoszam] || null
}

/**
 * Ellenőrzi hogy létezik-e az irányítószám
 * @param {string} iranyitoszam - 4 számjegyű irányítószám
 * @returns {boolean}
 */
export function isValidIranyitoszam(iranyitoszam) {
  return iranyitoszam && iranyitoszam.length === 4 && iranyitoszam in iranyitoszamMapping
}